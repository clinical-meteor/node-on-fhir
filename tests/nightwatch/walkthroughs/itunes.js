// add tests to this file using the Nightwatch.js API
// http://nightwatchjs.org/api

module.exports = {
  tags: ["itunes", "publish", "marketing"],
  "iTunesConnect" : function (client) {
    client
      .resizeWindow(1024, 768)

      .url("http://localhost:3000/about")
      .pause(500)
      .verify.elementPresent("#aboutPage")
      .saveScreenshot("tests/nightwatch/screenshots/iTunesConnect/A-aboutPage.png")

      .url("http://localhost:3000/support")
      .pause(500)
      .verify.elementPresent("#forumPage")
      .saveScreenshot("tests/nightwatch/screenshots/iTunesConnect/B-forumPage.png")

      .url("http://localhost:3000/privacy")
      .pause(500)
      .verify.elementPresent("#privacyPage")
      .saveScreenshot("tests/nightwatch/screenshots/iTunesConnect/C-privacyPage.png")

      .end();
  }
};
